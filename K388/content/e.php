	<h1>IT SPECIALIZATION</h1>
	<p>K388 are starting to simmer in real estate technology. The first phase of technology development in the category, which was primarily focused around listing services for the residential side of the market, has paved the way for industry leaders to broadly reconsider how technology can make their lives better.
	<br><br>
	For those of the people in the technology world with some background in real estate, the opportunity may seem obvious. But real estate is a sector of the economy that’s created immense wealth without changing their workflows or processes for many decades, so there’s a predisposed lack of urgency to upgrade the online tool belt.

		<br><br>
		<ul style="list-style-type:square">
			<li>Property Management</li>
			<br>
			<p>Several companies are already competing for dominance in this category, most offering software that helps property owners and management companies oversee and easily track commercial real estate assets. Industry-wide adoption is still sub-10 percent, though, so lots of opportunity for growth remains.</p>
			<br><br>
			<li>Research and Analytics</li>
			<br>
			<p>Traditionally, commercial real estate developers would hunker down with teams of analysts using HP calculators and gathering demographic research to evaluate an investment opportunity. Today, open data initiatives in municipalities across the country — combined with creative needle threading by software developers — is changing this landscape, and much of the data is readily available via monthly SaaS licenses.</p>
			<br><br>
			<li>Listing Services/Tech-Enabled Brokerages</li>
			<br>
			<p>Contrary to the incumbents in the residential market, which are predominantly media businesses generating revenue from advertising, a real opportunity exists for tech-enabled commercial listing services that could level the playing field, acting as marketplaces, and replacing the less efficient relationship-driven model that still persists today.</p>
			<br><br>
			<li>Residential and Commercial Lending</li>
			<br>
			<p>Regulatory changes have opened up opportunities for innovation in lending, and real estate lending is by far the largest sub-category. We’re starting to see a number of emerging companies target this area in different ways. Residential and commercial lending are different animals so my guess is that we’ll see a dozen worthwhile challengers going after each market.</p>
			<br>
		</ul>
	</p>
